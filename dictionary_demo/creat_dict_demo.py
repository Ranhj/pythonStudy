# {} 键值对    各个键值对用逗号隔开
# 创建有数据的字典
dict1 = {'name': 'Tom', 'age': 18, 'gender': ''}
print(dict1)                # {'name': 'Tom', 'age': 18, 'gender': ''}
print(type(dict1))          # <class 'dict'>
# 创建空字典
dict2 = {}
print(dict2)                # {}
print(type(dict2))          # <class 'dict'>
dict3 = dict()
print(dict3)                # {}
print(type(dict3))          # <class 'dict'>